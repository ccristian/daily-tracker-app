String? resolvePredefinedSystemKeyFromName(String name) {
  return predefinedActivityAliases[name.trim().toLowerCase()];
}

PredefinedActivitySpec? predefinedSpecBySystemKey(String systemKey) {
  for (final spec in predefinedActivitySpecs) {
    if (spec.systemKey == systemKey) {
      return spec;
    }
  }
  return null;
}
